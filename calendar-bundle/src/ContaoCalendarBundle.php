namespace Contao\CalendarBundle;

use Contao\CalendarBundle\DependencyInjection\Compiler\AddPermissionsListenerPass;
use Symfony\Component\DependencyInjection\Compiler\PassConfig;
use Symfony\Component\DependencyInjection\ContainerBuilder;
use Symfony\Component\HttpKernel\Bundle\Bundle;

class ContaoCalendarBundle extends Bundle
{
    public function getPath(): string
    {
        return \dirname(__DIR__);
    }

    public function build(ContainerBuilder $container): void
    {
        $container->addCompilerPass(new AddPermissionsListenerPass(), PassConfig::TYPE_BEFORE_OPTIMIZATION, 1);
    }
}
