namespace Contao\CoreBundle\Command;

use Contao\CoreBundle\Repository\AccessTokenRepository;
use Symfony\Component\Console\Attribute\AsCommand;
use Symfony\Component\Console\Command\Command;
use Symfony\Component\Console\Input\InputInterface;
use Symfony\Component\Console\Output\OutputInterface;
use Symfony\Component\Console\Style\SymfonyStyle;

#[AsCommand(
    name: 'contao:access-token:remove-expired',
    description: 'Removes expired access tokens.',
)]
class AccessTokenRemoveExpiredCommand extends Command
{
    public function __construct(
        private readonly AccessTokenRepository $accessTokenRepository,
    ) {
        parent::__construct();
    }

    protected function execute(InputInterface $input, OutputInterface $output): int
    {
        $io = new SymfonyStyle($input, $output);

        $this->accessTokenRepository->removeExpired();

        $io->success('Expired access tokens removed.');

        return Command::SUCCESS;
    }
}
